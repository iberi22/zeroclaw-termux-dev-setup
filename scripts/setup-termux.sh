#!/bin/bash
# ============================================================
# ZeroClaw SWAL Node - Termux Setup v4.1
# Minimal, fast, with SWAL status MOTD
# ============================================================

RED='\033[0;31m'; GREEN='\033[0;32m'; YELLOW='\033[1;33m'
CYAN='\033[0;36m'; NC='\033[0m'

log() { printf "%b[SWAL]%b %s\n" "$1" "$NC" "$2"; }
info()    { log "$CYAN" "$@"; }
success() { log "$GREEN" "$@"; }
warn()    { log "$YELLOW" "$@"; }
error()   { log "$RED" "$@"; }

CONFIG_FILE="$HOME/.zeroclaw/config.toml"
STATUS_SCRIPT="$HOME/.local/bin/swal-status.sh"

echo ""
echo "============================================================"
echo "  ZeroClaw SWAL Node - Termux Setup v4.1"
echo "============================================================"
echo ""

# ============================================================
# 1. FIX BROKEN CONFIG
# ============================================================
info "1/5 - Verificando configuraciones rotas..."

if [[ -f "$HOME/.bashrc" ]]; then
    if grep -q "zeroclaw start" "$HOME/.bashrc" 2>/dev/null; then
        warn "Encontrado 'zeroclaw start' en .bashrc - removiendo..."
        sed -i '/zeroclaw start/d' "$HOME/.bashrc"
        success "Reparado .bashrc"
    fi
fi

if [[ -f "$HOME/.zshrc" ]]; then
    if grep -q "zeroclaw start" "$HOME/.zshrc" 2>/dev/null; then
        warn "Encontrado 'zeroclaw start' en .zshrc - removiendo..."
        sed -i '/zeroclaw start/d' "$HOME/.zshrc"
        success "Reparado .zshrc"
    fi
fi

success "Configuraciones rotas verificadas"

# ============================================================
# 2. CLEAN MOTD
# ============================================================
info "2/5 - Limpiando mensajes de inicio..."
touch "$HOME/.hushlogin" 2>/dev/null || true
success "Mensajes limpiados"

# ============================================================
# 3. INSTALL SWAL STATUS SCRIPT
# ============================================================
info "3/5 - Instalando SWAL status dashboard..."

mkdir -p "$HOME/.local/bin"

# Detect if we're running in Docker (no /data/tmp, use /tmp)
if [[ -d "/data" ]]; then
    STATUS_SCRIPT_SOURCE="https://raw.githubusercontent.com/iberi22/zeroclaw-termux-dev-setup/master/scripts/swal-status.sh"
else
    STATUS_SCRIPT_SOURCE="https://raw.githubusercontent.com/iberi22/zeroclaw-termux-dev-setup/master/scripts/swal-status.sh"
fi

if command -v curl &>/dev/null; then
    if curl -fsSL "$STATUS_SCRIPT_SOURCE" -o "$STATUS_SCRIPT" 2>/dev/null; then
        chmod +x "$STATUS_SCRIPT" 2>/dev/null || true
        success "swal-status.sh instalado"
    else
        warn "No se pudo descargar swal-status.sh"
    fi
else
    warn "curl no disponible"
fi

# Add to MOTD (bashrc/zshrc)
MOTD_LINE='[[ -x "$HOME/.local/bin/swal-status.sh" ]] && "$HOME/.local/bin/swal-status.sh"'

if [[ -f "$HOME/.bashrc" ]] && ! grep -q "swal-status.sh" "$HOME/.bashrc" 2>/dev/null; then
    echo "" >> "$HOME/.bashrc"
    echo "# SWAL Status Dashboard" >> "$HOME/.bashrc"
    echo "$MOTD_LINE" >> "$HOME/.bashrc"
    success "MOTD agregado a .bashrc"
fi

if [[ -f "$HOME/.zshrc" ]] && ! grep -q "swal-status.sh" "$HOME/.zshrc" 2>/dev/null; then
    echo "" >> "$HOME/.zshrc"
    echo "# SWAL Status Dashboard" >> "$HOME/.zshrc"
    echo "$MOTD_LINE" >> "$HOME/.zshrc"
    success "MOTD agregado a .zshrc"
fi

# ============================================================
# 4. CONFIGURE ZEROCLAW
# ============================================================
info "4/5 - Configurando ZeroClaw..."

mkdir -p "$HOME/.zeroclaw"

cat > "$CONFIG_FILE" << 'CONFIGEOF'
# ZeroClaw Config - Generated by setup-termux.sh v4.1
# SWAL Node - SouthWest AI Labs

[agent]
autonomy_level = "full"

[security]
allowed_commands = ["pkg", "pkg_install", "pkg_update", "pkg_upgrade", "termux-setup-storage", "git", "curl", "wget", "bash", "sh", "echo", "pwd", "ls", "cd", "mkdir", "rm", "cp", "mv", "cat", "grep", "sed", "awk", "find", "npm", "node", "python", "python3", "pip", "pip3"]

[workspace]
path = "~/.zeroclaw/workspace"
CONFIGEOF

success "ZeroClaw configurado"

# ============================================================
# 5. SHOW RESULTS
# ============================================================
info "5/5 - Mostrando configuracion..."

echo ""
echo "============================================================"
echo "  CONFIGURACION DE SEGURIDAD ZEROCLAW"
echo "============================================================"
echo ""
echo "Archivo: $CONFIG_FILE"
echo ""
cat "$CONFIG_FILE"
echo ""
echo "============================================================"

if grep -q 'autonomy_level = "full"' "$CONFIG_FILE"; then
    success "autonomy_level = full OK"
else
    error "ERROR: autonomy_level no configurado!"
fi

if grep -q 'allowed_commands' "$CONFIG_FILE"; then
    success "allowed_commands OK"
else
    error "ERROR: allowed_commands no configurado!"
fi

echo ""
echo "============================================================"
echo "  SETUP COMPLETADO!"
echo "============================================================"
echo ""
info "Proximos pasos:"
echo "  1. Inicia zeroclaw: zeroclaw daemon"
echo "  2. O reinicia si ya estaba: pkill zeroclaw; zeroclaw daemon"
echo ""
echo "  Cada vez que abras Termux veras el dashboard SWAL."
echo "  Para ocultar: touch ~/.swal-motd-disable"
echo ""