#!/bin/bash
# ============================================================
# ZeroClaw SWAL Node - Termux Setup v3.10 (FIXED)
# ============================================================
set -x  # Debug mode

echo "============================================================"
echo "  ZeroClaw SWAL Node - Termux Setup v3.10 (FIXED)"
echo "============================================================"
echo "DEBUG: Starting at $(date)"

RED='\033[0;31m'; GREEN='\033[0;32m'; YELLOW='\033[1;33m'
CYAN='\033[0;36m'; NC='\033[0m'

log() { printf "%b[SWAL]%b %s\n" "$1" "$NC" "$2"; }
info()    { log "$CYAN" "$@"; }
success() { log "$GREEN" "$@"; }
warn()    { log "$YELLOW" "$@"; }
error()   { log "$RED" "$@"; }

OPENCLAW_DIR="$HOME/.openclaw"
WORKSPACE_DIR="$OPENCLAW_DIR/workspace"
ZEROCLAW_DIR="$HOME/.zeroclaw"
ENV_FILE="$OPENCLAW_DIR/secrets.env"
CONFIG_FILE="$HOME/.zeroclaw/config.toml"

echo "DEBUG: HOME=$HOME"
echo "DEBUG: CONFIG_FILE=$CONFIG_FILE"

# ============================================================
# FIX BROKEN CONFIG
# ============================================================
info "Buscando configuraciones rotas..."

if [[ -f "$HOME/.bashrc" ]]; then
    if grep -q "zeroclaw start" "$HOME/.bashrc" 2>/dev/null; then
        warn "Encontrado 'zeroclaw start' en .bashrc!"
        sed -i '/zeroclaw start/d' "$HOME/.bashrc"
        success "Reparado"
    fi
fi

if [[ -f "$HOME/.zshrc" ]]; then
    if grep -q "zeroclaw start" "$HOME/.zshrc" 2>/dev/null; then
        warn "Encontrado 'zeroclaw start' en .zshrc!"
        sed -i '/zeroclaw start/d' "$HOME/.zshrc"
        success "Reparado"
    fi
fi

info "No se encontraron configuraciones rotas"

# ============================================================
# CLEAN MOTD
# ============================================================
info "Limpiando mensajes de inicio..."
touch "$HOME/.hushlogin" 2>/dev/null || true
success "Mensajes limpiados"

# ============================================================
# CHECK PERMISSIONS
# ============================================================
info "Verificando permisos de Termux..."

if [[ -n "$TERMUX_VERSION" ]]; then
    info "Ejecutando en Termux"
    
    if [[ ! -d "$HOME/storage" ]]; then
        warn "Permiso de storage no detectado"
        termux-setup-storage -y 2>/dev/null || true
        sleep 2
    fi
fi

info "Verificando acceso a pkg..."
if pkg update -y &>/dev/null 2>&1; then
    PKG_ACCESS=true
    success "pkg tiene acceso completo"
else
    PKG_ACCESS=false
    warn "pkg tiene acceso limitado"
fi

# ============================================================
# CHECK SERVICES
# ============================================================
info "Verificando servicios..."

echo -n "  SSH server: "
if pgrep -f sshd &>/dev/null; then
    echo -e "${GREEN}OK${NC}"
elif command -v sshd &>/dev/null; then
    echo -e "${YELLOW}WARN${NC}"
else
    echo -e "${YELLOW}WARN${NC}"
fi

echo -n "  ZeroClaw daemon: "
if pgrep -f "zeroclaw daemon" &>/dev/null; then
    echo -e "${GREEN}OK${NC}"
elif command -v zeroclaw &>/dev/null; then
    echo -e "${YELLOW}WARN${NC}"
else
    echo -e "${YELLOW}WARN${NC}"
fi

# ============================================================
# CONFIGURE ZEROCLAW AUTONOMY AND SECURITY (FIXED - NO local AT TOP LEVEL)
# ============================================================
info "Configurando ZeroClaw..."

# Create directory
mkdir -p "$HOME/.zeroclaw"

# Initialize config file if it doesn't exist
if [[ ! -f "$CONFIG_FILE" ]]; then
    echo "# ZeroClaw Config - Generated by setup-termux.sh" > "$CONFIG_FILE"
    echo "DEBUG: Created new config file"
fi

# Add [agent] section and autonomy_level
if grep -q '^\[agent\]' "$CONFIG_FILE" 2>/dev/null; then
    echo "DEBUG: [agent] section exists"
    if grep -q 'autonomy_level' "$CONFIG_FILE" 2>/dev/null; then
        sed -i 's/autonomy_level = ".*"/autonomy_level = "full"/' "$CONFIG_FILE"
    else
        sed -i '/^\[agent\]/a autonomy_level = "full"' "$CONFIG_FILE"
    fi
else
    echo "" >> "$CONFIG_FILE"
    echo "[agent]" >> "$CONFIG_FILE"
    echo 'autonomy_level = "full"' >> "$CONFIG_FILE"
fi

# Add [security] section
if grep -q '^\[security\]' "$CONFIG_FILE" 2>/dev/null; then
    echo "DEBUG: [security] section exists"
    if ! grep -q 'allowed_commands' "$CONFIG_FILE" 2>/dev/null; then
        sed -i '/^\[security\]/a allowed_commands = ["pkg", "git", "curl", "wget", "bash", "sh", "echo", "pwd", "ls", "cd", "mkdir", "npm", "node", "python", "python3", "pip"]' "$CONFIG_FILE"
    fi
else
    echo "" >> "$CONFIG_FILE"
    echo "[security]" >> "$CONFIG_FILE"
    echo 'allowed_commands = ["pkg", "git", "curl", "wget", "bash", "sh", "echo", "pwd", "ls", "cd", "mkdir", "npm", "node", "python", "python3", "pip"]' >> "$CONFIG_FILE"
fi

success "ZeroClaw configurado"

# ============================================================
# SHOW CONFIG
# ============================================================
echo ""
echo "============================================================"
echo "  CONFIGURACION DE SEGURIDAD ZEROCLAW"
echo "============================================================"
echo ""

info "Archivo: $CONFIG_FILE"
echo ""
echo "--- Contenido completo ---"
cat "$CONFIG_FILE"
echo ""

echo "--- Resumen ---"
if grep -i "autonomy_level" "$CONFIG_FILE" 2>/dev/null; then
    :
else
    echo "  autonomy_level: NO CONFIGURADO"
fi

if grep "allowed_commands" "$CONFIG_FILE" 2>/dev/null; then
    :
else
    echo "  allowed_commands: NO CONFIGURADO"
fi

echo ""
echo "============================================================"
echo "  SETUP COMPLETADO!"
echo "============================================================"
echo ""
info "Log debug guardado en: /data/tmp/setup-debug.log"
echo ""
echo "Proximos pasos:"
echo "  1. Copia el config de arriba"
echo "  2. Reinicia zeroclaw: pkill zeroclaw; zeroclaw daemon"
echo ""

echo "DEBUG: Finished at $(date)"